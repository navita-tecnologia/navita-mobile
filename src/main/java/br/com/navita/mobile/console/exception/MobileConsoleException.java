package br.com.navita.mobile.console.exception;

public class MobileConsoleException extends Exception {

	public MobileConsoleException() {
		super();
		
	}

	public MobileConsoleException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public MobileConsoleException(String message) {
		super(message);
		
	}

	public MobileConsoleException(Throwable cause) {
		super(cause);
		
	}

}
