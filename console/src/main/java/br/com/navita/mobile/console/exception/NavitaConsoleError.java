package br.com.navita.mobile.console.exception;

public class NavitaConsoleError extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public NavitaConsoleError() {
		super();
		
	}

	public NavitaConsoleError(String message, Throwable cause) {
		super(message, cause);
		
	}

	public NavitaConsoleError(String message) {
		super(message);
		
	}

	public NavitaConsoleError(Throwable cause) {
		super(cause);
		
	}

}
